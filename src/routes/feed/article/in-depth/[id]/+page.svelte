        <p><strong>GoMining veTokenomics</strong></p>
        <p></p>
        <p>
            GOMINING is the native token of the GoMining platform and plays a
            crucial role in the veGOMINING token model. Accessible on both the
            Ethereum and Binance Smart Chain blockchains, GOMINING can be used
            for discounted electricity payments, staking, and in-app payments,
            like digital miner purchases and upgrades.
        </p>
        <p></p>
        <p>
            According to GoMining's unique deflationary-based token model, a
            portion of GOMINING's supply collected for electricity payments
            undergoes weekly burn and mint cycles. During these cycles, all
            collected tokens are burned and then minted back in a lesser amount.
            The amount of minted tokens is defined by the ratio of the ongoing
            epoch and cycle. Currently, the platform mints back 83% of the
            weekly collected amount.
        </p>
